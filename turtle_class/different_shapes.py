def change_pen_color(turtle = Turtle()):
    turtle.pencolor(random.choice(colors))


def draw_shape(sides, turtle = Turtle()):
    angle = 360/sides
    change_pen_color(turtle)
    for _ in range(sides):
        turtle.right(angle)
        turtle.forward(100)
    


